<h1>About xOS</h1>
<p>xOS is a webtop OS.</p>
<p>Definition:</p>
<p>A web desktop or webtop is a desktop environment embedded in a web browser or similar client application. A webtop integrates web applications, web services, clientâ€“server applications, application servers, and applications on the local client into a desktop environment using the desktop metaphor. Web desktops provide an environment similar to that of Windows, Mac, or a graphical user interface on Unix and Linux systems. It is a virtual desktop running in a web browser. In a webtop the applications, data, files, configuration, settings, and access privileges reside remotely over the network. Much of the computing takes place remotely. The browser is primarily used for display and input purposes. The terms "web desktop" and "webtop" are distinct from web operating system, a network operating system such as TinyOS or distributed operating system such as Inferno. In popular use, web desktops are sometimes referred to incorrectly as web operating systems or simply WebOS. -Wikipedia.org - Web Desktop - 26 of May, 2011</p>
